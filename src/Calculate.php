<?php

namespace Katharinadehn\Calculator;

class Calculate {
    public function add($x, $y) {
        return $x + $y;
    }

    public function multiply($x, $y) {
        return $x * $y;
    }

    public function subtrack($x, $y) {
        return $x - $y;
    }

    public function divide($x, $y) {
        return $x / $y;
    }
}

